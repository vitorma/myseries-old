package br.edu.ufcg.aweseries.thetvdb;

public class Series {
    private String id;
	private String name;

	public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String seriesName) {
        this.name = seriesName;
    }
}
